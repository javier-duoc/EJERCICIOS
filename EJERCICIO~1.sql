SELECT 
APELLIDO "APELLIDO DEL EMPLEADO",
OFICIO "CARGO DEL EMPLEADO",
DEPT_NO "NUMERO DE DEPARTAMENTO",
SALARIO * 1.05 AS "SALARIO + INCREMENTO 5%"
FROM EMPLE
ORDER BY APELLIDO, DEPT_NO DESC;


SELECT 
    DEPT_NO "NUMERO DE DEPARTAMENTO",
    DNOMBRE "NOMBRE DEL DEPARTAMENTO",
    LOC "UBICACION GEOGRAFICA"
    FROM DEPART
WHERE 
    LOC NOT IN ('MADRID', 'BILBAO');
    
----

SELECT 
    DEPT_NO "DEPARTAMENTO" , 
    EMP_NO || '-' || APELLIDO "EMPLEADO",
    SALARIO "SALARIO ACTUAL",
    SALARIO * 1.08 "INCREMENTO POR ANTIGUEDAD",
    SALARIO * 0.9991 "DESCUENTO MULTAS",
    OFICIO "CARGO"
FROM EMPLE
ORDER BY DEPT_NO DESC;

