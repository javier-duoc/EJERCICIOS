SELECT 
    CODPRODUCTO AS "ID",
    INITCAP(DESCRIPCION) AS "DESCRIPCION DE PRODUCTO",
    LPAD(NVL2(TO_CHAR(valorcompradolar),TO_CHAR(valorcompradolar) ||'USD','SIN REGISTRO'),15,' ') AS "
    LPAD (NVL2(TO_CHAR(valorcompradolar),TO_CHAR(ROUND(VALORCOMPRADOLAR * &TIPOCAMBIO_DOLAR),'$999g999')
    TOTALSTOCK AS "STOCK",
--VALIDACION STOCK

    CASE 
        WHEN TOTALSTOCK IS NULL THEN 'SIN DATOS'
        WHEN TOTALSTOCK < &UMBRAL_BAJO THEN '°ALERTA STOCK MUY BAJO!'
        WHEN TOTALSTOCK BETWEEN &UMBRAL_ALTO THEN '°REABASTECER PRONTO!'
        ELSE 'OK'
    END AS "ALERTA STOCK",
    
    -- DESCUENTO SUGERIDO
    
    LPAD (CASE
        WHEN TOTALSTOCK > 80 THEN TO_CHAR(VUNITARIO * 0.9, '$999G999')
        ELSE 'N/A'
    END,10, ' ') AS "PRECIO OFERTA"
FROM PRODUCTO
WHERE 
    DESCRIPCION LIKE 'ZAPATO%' AND PROCEDENCIA = 'I'
    PRDER BY "ID" DESC;

    