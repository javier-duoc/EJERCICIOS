SELECT
    NUMFACTURA "NUMERO DE FACTURA",
    TO_CHAR(FECHA, 'DD "DE" MONTH YYYY') "FECHA DE EMISION",
    LPAD (RUTCLIENTE,10,0) "RUT CLIENTE",
    TO_CHAR(NETO,'$9999999') AS "MONTO NETO",
    TO_CHAR(IVA,'$99999999') AS "MONTO IVA",
    TO_CHAR (TOTAL,'$9999999') AS "TOTAL FACTURA",
    
    CASE 
        WHEN TOTAL BETWEEN 0 AND 50000 THEN 'BAJO'
        WHEN TOTAL BETWEEN 50001 AND 100000 THEN 'MEDIO'
        ELSE 'ALTO'
    END AS "CATEGORIA MONTO",
    CASE 
        WHEN CODPAGO = 1 THEN 'EFECTIVO'
        WHEN CODPAGO = 2 THEN 'TARJETA DEBITO'
        WHEN CODPAGO = 3 THEN 'TARJETA DE CREDITO'
        WHEN CODPAGO = 4 THEN 'CHEQUE'
    END "FORMA DE PAGO"
    FROM FACTURA 
    WHERE 
        EXTRACT (YEAR FROM FECHA) = EXTRACT (YEAR FROM SYSDATE)-1
    ORDER BY FECHA DESC, "MONTO NETO" DESC;
    
