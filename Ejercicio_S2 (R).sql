SELECT 
    LPAD (RUTCLIENTE,12,'*') AS "RUT",
    INITCAP(NOMBRE)          AS "Cliente",
    --Manejo de telťfono y comuna nulos:
    NVL(TO_CHAR(TELEFONO), 'Sin telefono') AS Telefono, 
    DECODE(CODCOMUNA, NULL, 'SIN COMUNA', TO_CHAR(CODCOMUNA)) AS Comuna,
    ESTADO, 
    --CATEGORIZAR CREDITO:
    CASE 
        WHEN SALDO / CREDITO < 0.5 THEN 'BUENO(' || TO_CHAR (CREDITO - SALDO, '$9999999') || ')'
        WHEN SALDO / CREDITO BETWEEN 0.5 AND 0.8 THEN 'REGULAR (' || TO_CHAR(SALDO, '$9G999G999') || ')'
        ELSE 'CRITICO'
    END AS "ESTADO CREDITO",
    
    --VALIDAR CORREOS:
    
    DECODE (MAIL, 
        NULL, 'Correo no registado', 
        SUBSTR(MAIL, INSTR(MAIL,'@') + 1)) AS "DOMINIO CORREO"
    FROM CLIENTE
    WHERE 
        (ESTADO = 'A' AND CREDITO >0)
    ORDER BY 
        NOMBRE ASC;
        
        
