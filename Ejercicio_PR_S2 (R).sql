SELECT
     UPPER (NOMBRE) || ' ' || INITCAP (APELLIDO_PATERNO) || ' '  || INITCAP (APELLIDO_MATERNO) AS NOMBRE_COMPLETO,
     LENGTH (NOMBRE) + LENGTH (APELLIDO_PATERNO) + LENGTH (APELLIDO_MATERNO) AS "LARGO_NOMBRE",
     SUBSTR (NOMBRE, 1, 1) ||'.' || SUBSTR (APELLIDO_PATERNO, 1,1) || '.' || + SUBSTR (APELLIDO_MATERNO, 1,1) AS "INICIALES"
     FROM CLIENTE_CINE
WHERE NOMBRE LIKE 'C%';

