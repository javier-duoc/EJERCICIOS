SELECT 
    TO_CHAR (FECHA, 'DD"-"MM"-"YYYY hh24:mi') "FECHA FUNCION",
    TRIM (TO_CHAR(FECHA, 'DAY')) "DIA_SEMANA",
CASE
    WHEN TO_NUMBER (TO_CHAR (FECHA, 'HH24')) BETWEEN 0 AND 15 THEN 'MATINE'
    WHEN TO_NUMBER (TO_CHAR (FECHA, 'HH24')) BETWEEN 16 AND 20 THEN 'TARDE'
    WHEN TO_NUMBER (TO_CHAR (FECHA, 'HH24')) BETWEEN 21 AND 23 THEN 'NOCHE'
END AS FRANJA_HORARIA,
DECODE  
    (NVL (ID_SALA, 0), 1, 'SALA 1', 2, 'SALA 2', 3, 'SALA 3', 4, 'SALA4', 'SALA DESCONOCIDA') AS SALA
FROM FUNCION_CINE
    WHERE TRUNC(FECHA) = TO_DATE ('&FECHA_BUSQUEDA',' DD-MM-YYYY')
    ORDER BY fecha;


    
    





    
    
